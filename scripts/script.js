function getUnreachableRooms() {

  const fakeGrid = structuredClone(grid);

  const directions = [
    [-1, 0],
    [0, 1],
    [1, 0],
    [0, -1]
  ];

  let changed = true;

  while (changed) {

    changed = false;
    const toLock = [];

    for (let row = 0; row < GRID_HEIGHT; row++) {
      for (let col = 0; col < GRID_WIDTH; col++) {

        const room = fakeGrid[row][col];

        // locked/key become visited
        if (
          room.state === "locked" ||
          room.state === "key"
        ) {

          room.state = "visisted";

          // spread into neighbors
          for (const [dr, dc] of directions) {

            const nr = row + dr;
            const nc = col + dc;

            if (
              nr < 0 ||
              nr >= GRID_HEIGHT ||
              nc < 0 ||
              nc >= GRID_WIDTH
            ) {
              continue;
            }

            const neighbor =
              fakeGrid[nr][nc];

            if (!neighbor) {
              continue;
            }

            // unknown becomes locked
            if (
              neighbor.state === "unknown"
            ) {

              toLock.push(neighbor);
            }
          }
        }
      }
    }

    for (const room of toLock) {
      room.state = "locked";
      changed = true;
    }
  }


  let unreachable = 0;
  for (let row = 0; row < GRID_HEIGHT; row++) {
    for (let col = 0; col < GRID_WIDTH; col++) {
      const room = fakeGrid[row][col];

      if (room.state === "unknown") {
        unreachable++;
        grid[row][col].state = "unreachable"

        // debug overlay
        //alt1.overLayRect(0xffff00ff, room.x, room.y, room.width, room.height, 200, 1);

      }
    }
  }

  return unreachable;
}

